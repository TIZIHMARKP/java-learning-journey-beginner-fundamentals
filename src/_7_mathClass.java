

public  static void main() {
    //   THE MATH CLASS

    System.out.println(Math.abs(-10));  // abs turns a -ve number into +ve
    System.out.println(Math.max(6, 10));
    System.out.println(Math.min(6, 10));
    System.out.println((int) Math.pow(5, 2));  // (int) we are casting the result to an integer
    System.out.println(Math.sqrt(25));
    System.out.println(Math.PI);



}




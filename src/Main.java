

public  static void main() {
    //   COMPARISON OPERATORS
//    They allow us to compare values

//    Boolean Operators
    int johnAge = 18;
    int maryAge = 20;
    System.out.println(johnAge > maryAge);  // false
    System.out.println(johnAge < maryAge);  // true
    System.out.println(johnAge == maryAge); // false
    System.out.println(johnAge != maryAge); // true
    System.out.println(johnAge >= maryAge);
    System.out.println(johnAge <= maryAge);

//    Logical Operators











}



